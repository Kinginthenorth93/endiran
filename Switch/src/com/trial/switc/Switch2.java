package com.trial.switc;

public class Switch2 {
	public static void main(String[] args) {
		String student = "Endiran";
		
	switch(student) {
	case "Dinesh":
		System.out.println(student + " is from Bangalore");
		break;
	case "Endiran":
		System.out.println(student + " is from Chennai");
		break;
	case "Raja":
		System.out.println(student + " is from Delhi");
		}
	}

}
