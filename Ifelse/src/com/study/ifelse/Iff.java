package com.study.ifelse;

public class Iff {
	public static void main(String[] args) {
			int m = 100;
			
			if(m==50) {
				System.out.println("positive no");
			}
			else if(m<200) {
				System.out.println("correct"); 
			}
			else {
				System.out.println("negative no");
			}
	}
	

}
